package section02;

public class Variable {
    public static void main(String[] args) {
        // 변수(variable) : 변할 수 있는 수
        // 작명방식
        // - camelCase(lower camel case)
        int a; // 변수 선언
        a = 3; // a에 3을 할당(대입: assignment) : 초기화
        System.out.println(a);

        int b = 5; // 변수 정의
        System.out.println(b); // sout + tab키 자동 완성

        a = 10;
        System.out.println(a);
    }
    
}
